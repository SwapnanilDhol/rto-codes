export function getStateByCode(code: string): State | undefined {
  return indiaStatesWithDistricts.find(s => s.code === code);
}

export function getDistrictById(stateCode: string, districtId: string): { state: State; district: District } | undefined {
  const state = getStateByCode(stateCode);
  if (!state) return undefined;
  const district = state.districts.find(d => d.id === districtId);
  if (!district) return undefined;
  return { state, district };
}

